function mockScore(
  productId: string,
  baseScore: number,
  finalScore: number,
  label: SustainabilityScore["label"],
  color: SustainabilityScore["color"],
): SustainabilityScore {
  return {
    product_id: productId,
    base_score: baseScore,
    final_score: finalScore,
    label,
    color,
    factors: {
      transport: Math.round(finalScore * 0.8 + Math.random() * 20),
      packaging: Math.round(finalScore * 0.7 + Math.random() * 15),
      certifications: Math.round(finalScore * 0.9 + Math.random() * 10),
      brand_ethics: Math.round(finalScore * 0.85 + Math.random() * 10),
      production: Math.round(finalScore * 0.75 + Math.random() * 15),
      end_of_life: Math.round(finalScore * 0.8 + Math.random() * 10),
    },
    adjustments: [],
  };
}

function mockExternality(
  productId: string,
  cost: number,
  shelfPrice: number,
): Externality {
  return {
    product_id: productId,
    externality_cost: cost,
    breakdown: {
      carbon: { cost: cost * 0.35, kg_co2e: cost * 2.1 },
      water: { cost: cost * 0.25, litres: cost * 120, scarcity_multiplier: 1.2 },
      packaging: {
        cost: cost * 0.2,
        materials: [
          { material: "plastic", grams: 15, cost: cost * 0.12, recyclable_locally: true },
          { material: "cardboard", grams: 30, cost: cost * 0.08, recyclable_locally: true },
        ],
      },
      land_use: { cost: cost * 0.12, m2: cost * 0.8 },
      eutrophication: { cost: cost * 0.08, index: cost * 0.3 },
    },
    total_cost: shelfPrice + cost,
  };
}

